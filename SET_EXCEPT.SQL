-- SET EXCEPT

-- RETURNS ALL DISTINCT ROWS FROM THE FOIRST QUERY THAT ARE NOT FOUND IN SECOND QUERY

-- IT IS THE ONLY ONE WHERE THE ORDER OF QUERIES AFFECTS THE FINAL RESULT.

-- THE ORDER OF QUERIES IN A EXCEPT DOES AFFECT THE RESULT

-- TASK : FIND THE EMPLYOEE WHO ARE NOT CUSTOMERS AT THE SAME TIME

SELECT 
FirstName,
LastName
FROM Sales.Employees 

EXCEPT

SELECT 
FirstName,
LastName
FROM Sales.Customers


